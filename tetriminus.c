int	ft_nb_tetri(char *s)
{
	int i;
	int tetri;

	i = 0;
	tetri = 0;
	while (s[i] != '\0')
	{
		if (s[i] == '\n' && s[i + 1] == '\n' || s[i + 1] == '\0')
			tetri++;
		i++;
	}
	return (tetri);
}
